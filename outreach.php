<p>It had to be more than just robotics. The robotics team had to do more this year than just build robots. Cameron Ramos (12), president of the robotics club, felt like there should be more to the club. There was a duty they weren’t fulfilling. He knew there was a whole community in need of something different. 
</p>
<p>Last year, when Ramos became president of the club, he knew that the robotics team needed to give back more to the community. He thought about the future of the club. 
</p>
<p>He thought about how he, and many other members in the club, joined robotics in high school not knowing anything about building robots. 
</p>
<p>He thought about the younger kids, who may have not liked sports and needed different after school activities, such as robotics.
</p>
<p>So, this past summer, Ramos decided to make an outreach program for elementary, middle, and even high school robotics teams. 
</p>
<p>In the outreach program he envisioned, his robotics team would mentor other kids and teach them the skills they would need to build robots. His robotics team would also teach the kids other skills they’d need in competitions, such as learning how to present information about robots. 
</p>
<p>He hoped that by creating this outreach program, he could help inspire kids and realize their potential as scientists and engineers. 
</p>
<p>“Robotics club has traditionally been: you join the club [and] you build robots, but there is no real sense of giving back to the community,” Ramos said. “I wanted to do something more than that because I believe that teaching is one of the best ways to learn something like robotics.” 
</p>
<p>The first step to creating the outreach program was getting a team together. He needed his most interested club members to lend him a hand in his vision. 
</p>
<p>So, he sent out an email to all of his club members, and those who were interested  in the outreach became part of the team.
</p>
<p>This team and Ramos then contacted the team leaders, parent volunteers, at the schools they planned to help out at. They found out their schedules and made a spreadsheet for all the practice days of the schools. 
</p>
<p>Then, they delegated days for everyone in the club to help out at these various schools. 
</p>
<p>After a while, the outreach program was becoming so popular and successful in the club that Ramos decided to make it a requirement for membership.
</p>
<p>The team members try to help the kids as much as possible without doing it for them. Instead of telling them what to do, team members help the kids think of different solutions to difficult problems.<br />

</p>
<p>Massoud Maher (12), a member of robotics, enjoys seeing how smart the kids are and how, most of the time, they can figure out problems themselves. 
</p>
<p>“I keep the kids on task and guide them in the right direction,” Maher said. “So, I help them build stuff but don’t actually build. I help them program but don’t do it for them.” 
</p>
<p>Members like Maher also get to hang out with the kids, and become their role models. For Maher, one of his favorite parts is hearing how funny the kids are. 
</p>
<p>Members also learn things from the kids themselves. 
</p>
<p>“I’ve learned a lot of patience,” Victoria Tom (11) said. “I’ve forgotten how much energy kids can have. It’s worth it in the end, though, to see the kids put their ideas into action and see the robot coming together.
</p>
<p>As of now, the robotics club’s outreach program has helped 200 kids in their pursuit of building robots. The club has even created robotic teams, such as the one they helped establish in Park Village, in order to give more kids the chance to build robots. 
</p>
<p>Ramos wanted to stay local, especially because he knows that many of the kids he’s mentoring now will be future Wolverines like himself. 
</p>
<p>Maybe one day, he hopes, they’ll join robotics and represent the school. 
</p>
<p>“Robotics is just a way to teach kids that they don’t have to think people just admire people who can bounce a ball, but that these people can also admire a person who can put together a robot,” Ramos said. “From our mentorship, kids will be able to stay involved in high school and realize what they’re capable of doing.” 
</p>
<p>For elementary and middle school students, the main problem with starting robotics teams is that it is entirely made up from parent volunteers. These parents, according to Ramos, aren’t usually very experienced with robotics, especially not with all the different events open to the teams. 
</p>
<p>That’s where the outreach program steps in. 
</p>
<p>The outreach program hopes to help prepare their school teams for competitive events. The outreach program hopes to help prepare school teams for events like this in order to help them compete in this competitive environment. Ramos hopes to spark their competitive spirits to make kids more excited about robots. 
</p>
<p>Ramos also hopes to use the outreach program in the future to help out high school teams, such as Rancho Bernardo and Del Norte, so that they too can participate in the high school competitions. 
</p>
<p>For now though, Ramos is happy with the progress the outreach program has made. 
</p>
<p>For him, he just loves seeing kids get excited about robots. 
</p>
<p>“When you go to an outreach event and you see the kids looking up to, and relating to you, you can see them getting inspired, and you yourself get inspired,” Ramos said. 
</p>
